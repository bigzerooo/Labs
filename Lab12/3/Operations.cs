using System;
using System.Collections.Generic;
using System.Text;

namespace _3
{
    static public class Operations
    {
        static public int Addition(int a,int b)
        {
            return a + b;
        }
        static public int Substraction(int a,int b)
        {
            return a - b;
        }
        static public int Multiplication(int a,int b)
        {
            return a * b;
        }
        static public int Division(int a,int b)
        {
            return a / b;
        }
    }
}
